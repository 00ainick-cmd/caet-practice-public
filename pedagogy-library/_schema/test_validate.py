def test_validator_accepts_valid_chapter():
    """The exemplar is the gold standard; validator must accept it."""
    result = validate_chapter(EXEMPLAR)
    assert result.ok, (
        "Validator rejected the exemplar; the validator (or schema) is "
        "wrong, not the exemplar. Errors:\n  - "
        + "\n  - ".join(result.errors)
    )
    assert result.errors == []


def test_validator_rejects_missing_required_field():
    """Dropping the ``id`` frontmatter field must produce an error."""
    content = _strip_frontmatter_field(_read_exemplar(), "id")
    fixture = _write_fixture("missing_id.md", content)

    result = validate_chapter(fixture)
    assert not result.ok
    assert any("id" in e for e in result.errors), (
        f"Expected an error mentioning 'id'; got: {result.errors!r}"
    )


def test_validator_rejects_too_few_citations():
    """``key_sources`` with fewer than 5 entries must fail."""
    original = _read_exemplar()
    # Remove the last 4 source lines so only 3 remain (exemplar has 7).
    lines = original.splitlines(keepends=True)
    src_indices = [
        i for i, line in enumerate(lines) if line.lstrip().startswith("- \"")
        and 'doi' in line.lower()
    ]
    assert len(src_indices) >= 5, "Could not locate citation lines in exemplar."
    to_drop = set(src_indices[3:])  # keep first 3, drop the rest
    new_lines = [line for i, line in enumerate(lines) if i not in to_drop]
    fixture = _write_fixture("too_few_citations.md", "".join(new_lines))

    result = validate_chapter(fixture)
    assert not result.ok
    assert any("key_sources" in e for e in result.errors), (
        f"Expected an error mentioning 'key_sources'; got: {result.errors!r}"
    )


def test_validator_rejects_missing_section():
    """Removing ``## Quality signal`` must fail the section-order check."""
    original = _read_exemplar()
    lines = original.splitlines(keepends=True)
    # Find the Quality signal heading and drop through to the next H2.
    start = next(
        i for i, line in enumerate(lines)
        if line.rstrip().startswith("## Quality signal")
    )
    end = next(
        i for i in range(start + 1, len(lines))
        if lines[i].rstrip().startswith("## ")
    )
    new_lines = lines[:start] + lines[end:]
    fixture = _write_fixture("missing_quality_signal.md", "".join(new_lines))

    result = validate_chapter(fixture)
    assert not result.ok
    assert any("Quality signal" in e for e in result.errors), (
        f"Expected an error mentioning the missing 'Quality signal' section; "
        f"got: {result.errors!r}"
    )


def test_validator_rejects_too_short_file():
    """A chapter under 120 lines must fail the length check."""
    original = _read_exemplar()
    lines = original.splitlines(keepends=True)
    # Find the closing frontmatter delimiter; preserve frontmatter intact.
    delim_indices = [i for i, line in enumerate(lines) if line.rstrip() == "---"]
    assert len(delim_indices) >= 2, "Exemplar frontmatter delimiters not found."
    fm_end = delim_indices[1]

    head = lines[: fm_end + 1]
    # Add a tiny body to keep the file parseable but well under 120 lines.
    tail = [
        "\n",
        "# Stub\n",
        "\n",
        "## One-line claim\n",
        "Stub.\n",
        "\n",
        "## Evidence base\n",
        "Stub.\n",
        "\n",
        "## When to apply\n",
        "Stub.\n",
        "\n",
        "## When NOT to apply\n",
        "Stub.\n",
        "\n",
        "## How to apply\n",
        "Stub.\n",
        "\n",
        "## Common misapplications\n",
        "Stub.\n",
        "\n",
        "## Examples across domains\n",
        "Stub.\n",
        "\n",
        "## Quality signal\n",
        "Stub.\n",
        "\n",
        "## Cross-references\n",
        "Stub.\n",
    ]
    fixture = _write_fixture("too_short.md", "".join(head + tail))
    # Sanity check: the fixture really is short.
    assert len(fixture.read_text(encoding="utf-8").splitlines()) < 120

    result = validate_chapter(fixture)
    assert not result.ok
    assert any(
        "length" in e.lower() or "120" in e or "lines" in e.lower()
        for e in result.errors
    ), f"Expected an error mentioning length/lines; got: {result.errors!r}"


def test_validator_rejects_too_long_file():
    """A chapter over 200 lines must fail the length check."""
    original = _read_exemplar()
    lines = original.splitlines(keepends=True)
    # Append filler paragraphs after the existing body to push past 200 lines.
    filler = ["\nAdditional filler paragraph for length test.\n"] * 50
    fixture = _write_fixture("too_long.md", "".join(lines + filler))
    assert len(fixture.read_text(encoding="utf-8").splitlines()) > 200

    result = validate_chapter(fixture)
    assert not result.ok
    assert any(
        "length" in e.lower() or "200" in e or "lines" in e.lower()
        for e in result.errors
    ), f"Expected an error mentioning length/lines; got: {result.errors!r}"


def test_validator_rejects_bad_id_pattern():
    """``id`` containing uppercase or invalid characters must fail."""
    original = _read_exemplar()
    bad = original.replace("id: testing-effect", "id: Testing_Effect", 1)
    fixture = _write_fixture("bad_id.md", bad)

    result = validate_chapter(fixture)
    assert not result.ok
    assert any("id" in e for e in result.errors)
