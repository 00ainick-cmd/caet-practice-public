"""Tests for pedagogy-library/_schema/validate.py.

Each test exercises one failure mode the validator must catch. The exemplar
chapter (testing-effect.md) is the gold standard; fixtures are generated by
copying the exemplar and breaking exactly one thing.

Run from this directory:

    python -m pytest test_validate.py -v
"""

from __future__ import annotations

import shutil
from pathlib import Path

import pytest

from validate import validate_chapter

HERE = Path(__file__).resolve().parent
LIBRARY_ROOT = HERE.parent
EXEMPLAR = LIBRARY_ROOT / "01-learning-science" / "testing-effect.md"
FIXTURES_DIR = HERE / "test_fixtures"


# ---------------------------------------------------------------------------
# Fixture generation
# ---------------------------------------------------------------------------

def _read_exemplar() -> str:
    return EXEMPLAR.read_text(encoding="utf-8")


def _write_fixture(name: str, content: str) -> Path:
    FIXTURES_DIR.mkdir(parents=True, exist_ok=True)
    path = FIXTURES_DIR / name
    path.write_text(content, encoding="utf-8")
    return path


def _strip_frontmatter_field(content: str, field: str) -> str:
    """Remove a single top-level field from the YAML frontmatter block.

    Handles both scalar (``id: foo``) and block (``key_sources:\\n  - ...``)
    forms by deleting the field's header line plus any subsequent indented
    continuation lines until the next top-level key or the closing ``---``.
    """
    lines = content.splitlines(keepends=True)
    if not lines or lines[0].rstrip("\r\n") != "---":
        raise AssertionError("Exemplar missing opening frontmatter delimiter.")

    out: list[str] = [lines[0]]
    i = 1
    in_field = False
    while i < len(lines):
        line = lines[i]
        stripped = line.rstrip("\r\n")
        if stripped == "---":
            out.extend(lines[i:])
            return "".join(out)
        if in_field:
            # Continuation lines are indented or blank; non-indented non-blank
            # marks the start of the next top-level key.
            if line.startswith((" ", "\t")) or stripped == "":
                i += 1
                continue
            in_field = False
            # fall through to evaluate this line as a new key
        if stripped.startswith(f"{field}:"):
            in_field = True
            i += 1
            continue
        out.append(line)
        i += 1
    raise AssertionError("Exemplar missing closing frontmatter delimiter.")


